# Формат входных данных
# На вход поступают числа I и M - количество строк и столбцов в поле. Далее дано число W - количество мин на поле.
# На следующих W строках выводятся пары чисел - координаты мин на на поле.
# Формат результата
# Требуется вывести на экран поле. Формат вывода указан в примере (мина обозначается символом «*»).
# Примеры
# Входные данные
# 3 2
# 2
# 1 1
# 2 2
# Результат работы
# * 2
# 2 *
# 1 1
#
#
# Примечания
# Примечание для зумеров: Поле в сапере состоит из ячеек, содержащих мины или цифры. Мины обозначаются символом «*», а
# цифра в ячейке означает количество мин, находящихся в соседних 8 ячейках. Если вокруг ячейки мин нет, то в ячейке
# должно быть написано О.
# Важное примечание: Ячейки должны быть разделены пробелами, каждая строка поля должна заканчиваться пробелом


I, M = map(int, input().split())
W = int(input())

field = [['0' for _ in range(M)] for _ in range(I)]

for _ in range(W):
    x, y = map(int, input().split())
    field[x-1][y-1] = '*'

for i in range(I):
    for j in range(M):
        if field[i][j] != '*':
            count = 0
            for dx in [-1, 0, 1]:
                for dy in [-1, 0, 1]:
                    if dx == 0 and dy == 0:
                        continue
                    nx, ny = i + dx, j + dy
                    if 0 <= nx < I and 0 <= ny < M and field[nx][ny] == '*':
                        count += 1
            field[i][j] = str(count)

for row in field:
    print(' '.join(row))
